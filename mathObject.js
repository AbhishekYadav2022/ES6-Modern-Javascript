// Math Object in ES6 

// sign()
let num1 = 5;
let num2 = -6;
let name = "Abhishek";

console.log(Math.sign(num1));
console.log(Math.sign(num2));
console.log(Math.sign(name));

// trunc()

let number1 = -5.998;
let number2 = 5.998;
console.log(Math.trunc(number1));
console.log(Math.floor(number1));

console.log(Math.trunc(number2));
console.log(Math.floor(number2));